package day01;
public class _003运算符 {

    public static void main(String[] args) {
        print();

    }
    // 需求：来一个方法，搞清楚基本的算术运算符
    public static void print() {
        int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }

    
}